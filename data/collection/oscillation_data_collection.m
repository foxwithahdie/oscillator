function oscillation_data_collection()
    oscillation_data = readtable("data/oscillation/oscillation_data.xls", ...
            VariableNamingRule='preserve');
    
    time = oscillation_data.("Time (s)");
    linear_accel_x = oscillation_data.("Linear Acceleration x (m/s^2)");
    linear_accel_y = oscillation_data.("Linear Acceleration y (m/s^2)");
    linear_accel_z = oscillation_data.("Linear Acceleration z (m/s^2)");
    absolute_accel = oscillation_data.("Absolute acceleration (m/s^2)");

    save("data/oscillation/oscillation_data.mat", ...
            "time", ...
            "linear_accel_x", ...
            "linear_accel_y", ...
            "linear_accel_z", ...
            "absolute_accel" ...
        )
end